import { readFileSync, writeFileSync, readdirSync, statSync } from 'node:fs'
import { join, relative, dirname } from 'node:path'

const SCREENS_DIR = 'src/screens'
const HEADER = '// Auto-generated by scripts/generate-screen-tests.mjs — do not edit manually'

function findScenariosFiles(dir) {
  const results = []
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry)
    if (entry === '_test-helpers' || entry === '_shared') continue
    if (statSync(full).isDirectory()) {
      results.push(...findScenariosFiles(full))
    } else if (entry === 'scenarios.ts') {
      results.push(full)
    }
  }
  return results
}

/**
 * Extract keys at depth 0 within a block (content between matching braces).
 * Only matches keys followed by `: {` or `: value` patterns.
 */
function extractTopLevelKeys(block) {
  const keys = []
  let depth = 0
  let i = 0
  while (i < block.length) {
    if (block[i] === '{') { depth++; i++; continue }
    if (block[i] === '}') { depth--; i++; continue }
    // Only look for keys at depth 0 (direct children)
    if (depth === 0 && (block[i] === '\n' || i === 0)) {
      const lineStart = block[i] === '\n' ? i + 1 : i
      const line = block.slice(lineStart)
      // Match: `  keyName: {` or `  'key-name': {`
      const match = line.match(/^\s+(?:'([\w-]+)'|(\w[\w-]*))\s*:\s*\{/)
      if (match) {
        keys.push(match[1] || match[2])
      }
    }
    i++
  }
  return keys
}

function parseScenarios(filePath) {
  const content = readFileSync(filePath, 'utf8')
  const screenDir = dirname(filePath)
  const rel = relative(SCREENS_DIR, screenDir) // e.g., "prescription/scan"
  const parts = rel.split('/')
  const screenName = parts[parts.length - 1]

  const hasRegions = /export\s+const\s+regions\s*=/.test(content)
  const hasScenarios = /export\s+const\s+scenarios\s*=/.test(content)

  if (hasRegions) {
    // Find the regions object block
    const regionsStart = content.search(/export\s+const\s+regions\s*=\s*\{/)
    if (regionsStart === -1) return null
    const braceStart = content.indexOf('{', regionsStart)
    let depth = 1
    let pos = braceStart + 1
    while (depth > 0 && pos < content.length) {
      if (content[pos] === '{') depth++
      if (content[pos] === '}') depth--
      pos++
    }
    const regionsBlock = content.slice(braceStart + 1, pos - 1)

    // Extract top-level region keys (direct children of regions object)
    const regionKeys = extractTopLevelKeys(regionsBlock)

    const regions = []
    for (const regionKey of regionKeys) {
      // Find this region's block in the content
      const regionPattern = new RegExp(`${regionKey}:\\s*\\{`)
      const regionMatch = regionsBlock.match(regionPattern)
      if (!regionMatch) continue

      // Extract label
      const regionStart = regionMatch.index
      const labelMatch = regionsBlock.slice(regionStart).match(/label:\s*'([^']+)'/)
      if (!labelMatch) continue
      const label = labelMatch[1]

      // Find the states block within this region
      const statesMatch = regionsBlock.slice(regionStart).match(/states:\s*\{/)
      if (!statesMatch) continue

      const statesStart = regionStart + statesMatch.index + statesMatch[0].length
      // Find matching closing brace for states block
      let stDepth = 1
      let stPos = statesStart
      const searchIn = regionsBlock
      while (stDepth > 0 && stPos < searchIn.length) {
        if (searchIn[stPos] === '{') stDepth++
        if (searchIn[stPos] === '}') stDepth--
        stPos++
      }
      const statesBlock = searchIn.slice(statesStart, stPos - 1)

      // Extract state keys using depth tracking
      const stateKeys = extractTopLevelKeys(statesBlock)

      if (stateKeys.length > 0) {
        regions.push({ key: regionKey, label, stateKeys })
      }
    }

    if (regions.length > 0) {
      return { type: 'regions', screenDir, screenName, describeName: rel, regions }
    }
  }

  if (hasScenarios) {
    // Find the scenarios object block
    const scenariosStart = content.search(/export\s+const\s+scenarios\s*=\s*\{/)
    if (scenariosStart === -1) return null

    const braceStart = content.indexOf('{', scenariosStart)
    let depth = 1
    let pos = braceStart + 1
    while (depth > 0 && pos < content.length) {
      if (content[pos] === '{') depth++
      if (content[pos] === '}') depth--
      pos++
    }
    const block = content.slice(braceStart + 1, pos - 1)

    // Extract top-level keys using depth tracking (avoids matching nested `data: {`)
    const stateKeys = extractTopLevelKeys(block)

    return { type: 'scenarios', screenDir, screenName, describeName: rel, stateKeys }
  }

  return null
}

function computeFixturesImport(screenDir) {
  const fixturesPath = join(SCREENS_DIR, '_test-helpers/fixtures')
  const rel = relative(screenDir, fixturesPath).replace(/\\/g, '/')
  return rel
}

function generateSpec(info) {
  const fixturesImport = computeFixturesImport(info.screenDir)
  const lines = [HEADER, '']
  lines.push(`import { test, expect } from '${fixturesImport}'`)
  lines.push('')
  lines.push(`test.describe('${info.describeName}', () => {`)
  lines.push(`  test.beforeEach(async ({ screen }) => {`)
  lines.push(`    await screen.select('${info.describeName}')`)
  lines.push(`  })`)

  if (info.type === 'scenarios') {
    for (const key of info.stateKeys) {
      lines.push('')
      lines.push(`  test('${key} — renders without error', async ({ screen }) => {`)
      lines.push(`    await screen.switchState('${key}')`)
      lines.push(`    await expect(screen.frame).not.toBeEmpty()`)
      lines.push(`  })`)
    }
  } else if (info.type === 'regions') {
    for (const region of info.regions) {
      for (const state of region.stateKeys) {
        lines.push('')
        lines.push(`  test('${region.key}:${state} — renders without error', async ({ screen }) => {`)
        lines.push(`    await screen.switchRegionState('${region.label}', '${state}')`)
        lines.push(`    await expect(screen.frame).not.toBeEmpty()`)
        lines.push(`  })`)
      }
    }
  }

  lines.push('})')
  lines.push('')
  return lines.join('\n')
}

// Main
const files = findScenariosFiles(SCREENS_DIR)
let generated = 0
let skipped = 0

for (const file of files) {
  const info = parseScenarios(file)
  if (!info) {
    console.log(`  skip: ${file} (no scenarios or regions found)`)
    skipped++
    continue
  }
  const specPath = join(info.screenDir, `${info.screenName}.spec.ts`)
  const content = generateSpec(info)
  writeFileSync(specPath, content, 'utf8')
  const stateCount = info.type === 'scenarios'
    ? info.stateKeys.length
    : info.regions.reduce((sum, r) => sum + r.stateKeys.length, 0)
  console.log(`  write: ${specPath} (${stateCount} tests)`)
  generated++
}

console.log(`\nDone: ${generated} spec files generated, ${skipped} skipped`)
